CREATE TABLE customers (
    cpf VARCHAR(11) PRIMARY KEY,
    name VARCHAR(120) NOT NULL,
    birth_date DATE NOT NULL,
    email VARCHAR(255) NOT NULL
);

CREATE TABLE complaint_statuses (
    id INTEGER PRIMARY KEY,
    name VARCHAR(30) NOT NULL UNIQUE
);

CREATE TABLE categories (
    id BIGINT PRIMARY KEY,
    name VARCHAR(80) NOT NULL UNIQUE
);

CREATE TABLE category_keywords (
    id BIGINT PRIMARY KEY,
    category_id BIGINT NOT NULL,
    keyword_value VARCHAR(120) NOT NULL,
    CONSTRAINT fk_category_keywords_category
        FOREIGN KEY (category_id) REFERENCES categories (id)
);

CREATE TABLE complaints (
    id UUID PRIMARY KEY,
    customer_cpf VARCHAR(11) NOT NULL,
    status_id INTEGER NOT NULL,
    complaint_date DATE NOT NULL,
    complaint_text VARCHAR(4000) NOT NULL,
    registered_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT fk_complaints_customer
        FOREIGN KEY (customer_cpf) REFERENCES customers (cpf),
    CONSTRAINT fk_complaints_status
        FOREIGN KEY (status_id) REFERENCES complaint_statuses (id)
);

CREATE TABLE complaint_categories (
    complaint_id UUID NOT NULL,
    category_id BIGINT NOT NULL,
    PRIMARY KEY (complaint_id, category_id),
    CONSTRAINT fk_complaint_categories_complaint
        FOREIGN KEY (complaint_id) REFERENCES complaints (id),
    CONSTRAINT fk_complaint_categories_category
        FOREIGN KEY (category_id) REFERENCES categories (id)
);

CREATE TABLE complaint_documents (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    complaint_id UUID NOT NULL,
    document_url VARCHAR(1000) NOT NULL,
    CONSTRAINT fk_complaint_documents_complaint
        FOREIGN KEY (complaint_id) REFERENCES complaints (id)
);

